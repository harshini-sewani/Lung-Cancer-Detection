    public static boolean isPhoneNumberValid(String phoneNumber) {
        boolean isValid = false;

        String expression = "[789]{1}[0-9]{9}";//this to validate only 8 digit and 10 digit numbers
        CharSequence inputStr = phoneNumber;
        Pattern pattern = Pattern.compile(expression);
        Matcher matcher = pattern.matcher(inputStr);
        if (matcher.matches()) {
            isValid = true;
        }
        return isValid;
    }

    public static boolean isEmailValid(String email) {
        boolean isValid = false;

        //Initialize reg ex for email.  
        String expression = "\\b[A-Za-z0-9._%+-]+@[A-Za-z.]+\\.[A-Za-z]{2,3}\\b";
        CharSequence inputStr = email;
        //Make the comparison case-insensitive.  
        Pattern pattern = Pattern.compile(expression, Pattern.CASE_INSENSITIVE);
        Matcher matcher = pattern.matcher(inputStr);
        if (matcher.matches()) {
            isValid = true;
        }
        return isValid;
    }

    public static boolean isValidpancard(String pancard) {
        boolean isValid = false;

        //Initialize reg ex for PANCARD NO.
        String expression = "[A-Z]{5}[0-9]{4}[A-Z]{1}";
        CharSequence inputStr = pancard;
        //Make the comparison case-insensitive.  
        Pattern pattern = Pattern.compile(expression, Pattern.CASE_INSENSITIVE);
        Matcher matcher = pattern.matcher(inputStr);
        if (matcher.matches()) {
            isValid = true;
        }
        return isValid;
    }

