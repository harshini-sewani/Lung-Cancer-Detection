    public int getAgeFromDOB(String DOB) {
        Calendar now = Calendar.getInstance();
        Calendar dob = Calendar.getInstance();
//    	      //take substrings of the dob so split out year, month and day
        int y = Integer.parseInt(DOB.substring(6, 10));
        int m = Integer.parseInt(DOB.substring(3, 5));
        int d = Integer.parseInt(DOB.substring(0, 2));
        dob.set(y, m, d);
        if (dob.after(now)) {
            System.out.println("here low date");
        }

        int year1 = now.get(Calendar.YEAR);
        int year2 = dob.get(Calendar.YEAR);
        int age = year1 - year2;
        int month1 = now.get(Calendar.MONTH);
        int month2 = dob.get(Calendar.MONTH);

        if (month2 > month1) {
            age--;
        } else if (month1 == month2) {
            int day1 = now.get(Calendar.DAY_OF_MONTH);
            int day2 = dob.get(Calendar.DAY_OF_MONTH);
            if (day2 > day1) {
                age--;
            }
        }
        System.out.println("Age is : " + age);
        return 18;
    }
